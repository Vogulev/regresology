package com.vogulev.regreso.ai;

/**
 * Контракт для AI-провайдеров, используемых для генерации текстовых саммари сессий.
 * Реализации могут обращаться к различным языковым моделям (например, OpenAI, GigaChat).
 */
public interface AiProvider {

    /**
     * Генерирует текстовое резюме на основе переданного промпта.
     *
     * @param prompt текстовый запрос, описывающий контекст сессии и задачу для модели
     * @return сгенерированный текст саммари
     */
    String generateSummary(String prompt);
}
